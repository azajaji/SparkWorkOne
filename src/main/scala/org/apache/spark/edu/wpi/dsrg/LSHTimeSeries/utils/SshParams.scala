  private def distData(t: AbstractRealDistribution): Array[Array[Double]] = {


    val result = Array.ofDim[Double](samplingSize, dictLength)
    for (s <- 0 until samplingSize) {
      for (k <- 0 until dictLength) {
        result(s)(k) = t.sample()
      }
    }
    result
  }

